  /*! \page breve_descripcion <center>Breve descripción</center>
  Las Vegas is being attacked by asteroids. The main goal of the game is
  to save Las Vegas by destroying the asteroids.  You must avoid the
  asteroids to reach the ground by placing bombs around the sky in order
  to destroy them. Your final score is determined by:
  -the numbers of destroyed asteroids
  -precision at destroying asteroids
  -survival time measured in days
  */

