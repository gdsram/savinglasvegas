//-----------------------------------------------------------

  /*! \page how_to_play <center>How to play</center>
  The first times you play, some hints will be shown for you to
  undertand how to use the weapons and how to pick parachutes up. The
  game will become harder and you will be able to pick new weapons up.
  The screen become wider and you have to scroll the screen in order to
  defend the whole city.
  */

